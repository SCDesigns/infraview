            Applications and Installations
          </h2>
          <ul class="common-BodyText">
            <li><span class="alpha">Startup Temperature:</span> Monitor flue gas temperature ramp from 250°F to 1,000°F for boiler startup preventing overfiring that could damage superheat tubes, or underfiring that could potentially send wet steam to the steam turbine damaging buckets.</li>
            <br>
            <li><span class="alpha">Soot Blower Control:</span> Record time vs. temperature histories to improve boiler performance and control of soot blower operation and duration. Monitor thermal transients during cleaning to improve heat transfer on boiler tubes from under cleaning which could lead to slagging conditions. Monitor boiler gas temperature for comparison to steam outlet temperature for determining soot blower activation.</li>
            <br>
            <li><span class="alpha">Low NOx Applications:</span> Sense temperature "window" set points between 1,600°F and 2,100°F for the injection of low NOx enhancers with urea or ammonia in Selective Noncatalytic Reduction Systems.</li>
            <br>
            <li><span class="alpha">Reduce Slagging / Ash Fusion:</span> Monitor maximum allowable temperature in the boiler superheat/reheat section for ash fusion alarm point annuciation.</li>
            <br>
            <li><span class="alpha">FEGT High Temperature Alarm:</span> Monitor highest temperature and maximum load condition for most efficient operation and prevent boiler waterwall, reheat and superheat tube failure.</li>
            <br>
            <li><span class="alpha">Reduce Fuel Consumption on Startup:</span> Monitor fuel changeover based on temperature rather than load, thereby reducing startup time by hours. This saving alone will pay for the Infra-View.</li>
            <br>
            <li><span class="alpha">Fluidized Bed Boilers:</span> Determine optimal temperature for pulverized limestone injection on flue gas desulfurization systems and general operating conditions. Measure bed temperature for optimal operating efficiency.</li>
            <br>
            <li><span class="alpha">Primary Furnace Area (B&W/FW):</span> Monitor flue gas temperature in separate sections of boilers with division walls to ensure temperature balance.</li>
            <br>
            <li><span class="alpha">Retractable Thermoprobes:</span> Replace high maintenance mechanical chain driven thermoprobes with low maintenance Infra-View non-contact infrared temperature monitor that has no moving parts.</li>
            <br>
            <li><span class="alpha">Fireball Centering (ABB-CE):</span> Identify fireball centering problems on tangentially fired boilers for control with burner corner dampers by locating two Infra-View Boiler thermometers on same side of boiler.</li>
            <br>
            <li><span class="alpha">Waste /Refuse Incinerators:</span> Monitor compliance temperature for incineration of toxic wastes at 1800°,F Infra-View can be certified to NIST standards.</li>
            <br>
            <li><span class="alpha">Black Liquor Recovery Boilers in Pulp/ Paper Mills:</span> The application of the Infra-View® Boiler Thermometer on Black Liquor Recovery Boilers in the Pulp & Paper industry can provide the operator with the following benefits. When installed ahead of the superheater, it replaces the traversing Thermo probe (tm) while at the same time giving a more consistent and accurate measurement of the superheater inlet temperature. When installed at the superheater inlet, the Infra-View® provides the operator with an accurate measurement of the combustion gasses in the upper furnace. This information is useful for the operator to adjust combustion conditions. The installation of the Infra-View® between the superheater and the generating bank provides the operator real time temperature data to determine the amount of pluggage in the superheater and to tell how close the flue-gases temperature is to the ash (salt cake) sticky temperature.The temperature of the gas in the recovery boiler is critical to its efficient operation and performance. Excessive temperatures can cause smelting or cindering of black liquor particles and can fuse to superheat and reheat tubes thereby decreasing the heat rate of the boiler from plugging of heat transfer surfaces.
            </li>
